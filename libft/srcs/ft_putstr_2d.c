#include "libft.h"

void	ft_putstr_2d(char **s)
{
	int i;

	i = 0;
	if (!s)
		return ;
	while (s[i] != 0)
	{
		ft_putstr(s[i]);
		ft_putchar('\n');
		i++;
	}
}

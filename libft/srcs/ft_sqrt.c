#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	while (i * i != nb && i <= 46340)
	{
		if ((i * i) > nb)
			return (0);
		i++;
	}
	if (i > 46340)
		return (0);
	return (i);
}

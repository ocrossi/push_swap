#include "libft.h"

void	ft_striter(char *s, void (*f) (char *))
{
	size_t	l;

	if (!(s) || !(f))
		return ;
	l = ft_strlen(s);
	while (l)
	{
		f(s);
		s++;
		l--;
	}
}
